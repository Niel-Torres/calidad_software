package com.example.grupo7.comunio.CardsList;

public class PlayerItem {
    private String playerName;
    private String playerData;
    private int playerFoto;

    public String getPlayerName() {
        return playerName;
    }

    public void setPlayerName(String name) {
        this.playerName = name;
    }

    public String getPlayerData() {
        return playerData;
    }

    public void setPlayerData(String data) {this.playerData = data; }

    public int getPlayerFoto() {
        return playerFoto;
    }

    public void setPlayerFoto(int ident) {
        this.playerFoto = ident;
    }

}
